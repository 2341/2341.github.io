function obfuscateText() {
    var oldtext = document.getElementById("1").value.toLowerCase();
    var newtext = "";
    for (index = 0; index < oldtext.length; index++) {
        if (typeof Homoglyph[oldtext[index]] === "undefined") {
            newtext += oldtext[index];
        } else {
            var rand = getRandomInt(0, Homoglyph[oldtext[index]].length);
            newtext += Homoglyph[oldtext[index]][rand];
        }
    }
    document.getElementById("1").value = newtext;
}
function getRandomInt(min, max) {
    return Math.floor(Math.random() * (max - min)) + min;
}
document.getElementById("2").onclick = obfuscateText;
